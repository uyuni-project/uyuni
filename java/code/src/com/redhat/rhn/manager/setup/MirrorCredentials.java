package com.redhat.rhn.manager.setup;

/**
 * Representation of a pair of mirror credentials (for either NCC or SCC).
 */
public class MirrorCredentials {

    private String user;
    private String password;
    private String email;

    /**
     * Constructor.
     * @param userIn
     * @param passwordIn
     * @param emailIn
     */
    public MirrorCredentials(String userIn, String passwordIn, String emailIn) {
        this.setUser(userIn);
        this.setPassword(passwordIn);
        this.setEmail(emailIn);
    }

    /**
     * @return the user
     */
    public String getUser() {
        return user;
    }

    /**
     * @param user the user to set
     */
    public void setUser(String user) {
        this.user = user;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }
}
