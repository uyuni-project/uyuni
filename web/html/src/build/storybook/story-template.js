import path from "node:path";

import { storyGroupName } from "./example-discovery.js";

const isAsciiLetter = (character) => (character >= "a" && character <= "z") || (character >= "A" && character <= "Z");

const isAsciiAlphaNumeric = (character) => isAsciiLetter(character) || (character >= "0" && character <= "9");

const withoutExtension = (filePath) => filePath.replace(/\.(tsx|ts)$/, "");

const withoutExampleSuffix = (fileName) => fileName.replace(/\.example\.(tsx|ts)$/, "");

export const storyExportName = (input) => {
  let normalized = "";

  for (const character of input) {
    if (isAsciiAlphaNumeric(character)) {
      normalized += character;
    } else if (normalized.length > 0 && !normalized.endsWith("_")) {
      normalized += "_";
    }
  }

  if (normalized.endsWith("_")) {
    normalized = normalized.slice(0, -1);
  }

  const exportName = normalized.charAt(0).toUpperCase() + normalized.slice(1);
  return isAsciiLetter(exportName.charAt(0)) || exportName.startsWith("_") ? exportName : `Story_${exportName}`;
};

export const storyTemplate = (relativePath) => {
  const importPath = withoutExtension(relativePath);
  const storyName = withoutExampleSuffix(path.posix.basename(relativePath));
  const title = `Legacy Example Stories/${storyGroupName(relativePath)}`;
  const exportName = storyExportName(storyName);

  return `/* eslint-disable */
/**
 * This file is generated by web/html/src/build/storybook/generate-legacy-stories.js.
 * Edit the source .example.tsx file or replace it with a real .stories.tsx file instead.
 */
import Component from "${importPath}";

const meta = {
  title: "${title}",
  component: Component,
  tags: ["!autodocs"],
  parameters: {
    docs: {
      description: {
        component: "Legacy story generated from ${relativePath}.",
      },
    },
  },
};

export default meta;

export const ${exportName} = {
  name: "${storyName}",
};
`;
};
