package com.suse.scc.client;

/**
 * Class representing proxy settings to be used with {@link SCCClient}.
 */
public class SCCProxySettings {
    private String hostname;
    private String username;
    private String password;
    private int port;

    public SCCProxySettings(String hostname, int port) {
        this.hostname = hostname;
        this.port = port;
    }

    public SCCProxySettings(String hostname, int port, String username, String password) {
        this(hostname, port);
        this.username = username;
        this.password = password;
    }

    /**
     * @return the hostname
     */
    public String getHostname() {
        return hostname;
    }

    /**
     * @param hostname the hostname to set
     */
    public void setHostname(String hostname) {
        this.hostname = hostname;
    }

    /**
     * @return the proxy port
     */
    public int getPort() {
        return port;
    }

    /**
     * @param port the port to set
     */
    public void setPort(int port) {
        this.port = port;
    }

    /**
     * @return the username
     */
    public String getUsername() {
        return username;
    }

    /**
     * @param username the username to set
     */
    public void setUsername(String username) {
        this.username = username;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }
}
